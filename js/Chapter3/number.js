//integer
let integer = 42;
console.log(integer); // Output: 42
//float
let float = 3.14;
console.log(float); // Output: 3.14
//EN
let exponential = 1.23e4;
console.log(exponential); // Output: 12300
//hex
let hex = 0x1a;
console.log(hex); // Output: 26
//binary
let binary = 0b1010;
console.log(binary); // Output: 10
//octal
let octal = 0o52;
console.log(octal); // Output: 42
// Special Numeric Values
// Infinity
let infinity = Infinity;
console.log(infinity); // Output: Infinity
// Negative Infinity
let negativeInfinity = -Infinity;
console.log(negativeInfinity); // Output: -Infinity
//NaN (Not-a-Number)
let notANumber = NaN;
console.log(notANumber); // Output: NaN
