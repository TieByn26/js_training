//Single Quotes
let singleQuoteString = 'Hello, world!';
console.log(singleQuoteString); // Output: Hello, world!
//Double Quotes
let doubleQuoteString = "Hello, world!";
console.log(doubleQuoteString); // Output: Hello, world!
//Backticks (Template Literals)
let name = 'John';
let templateLiteral = `Hello, ${name}!`;
console.log(templateLiteral); // Output: Hello, John!
//Multiline Strings (using Template Literals)
let multilineString = `This is a
multiline string.`;
console.log(multilineString);//Output:This is amultiline string.
//Escaping Characters
let escapingCharacters = 'It\'s a beautiful day!';
console.log(escapingCharacters);
